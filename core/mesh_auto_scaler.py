if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO, format="%(levelname)s %(name)s — %(message)s")

    import tempfile

    # Create a temporary mesh directory with synthetic nodes.json
    with tempfile.TemporaryDirectory() as tmp:
        log_dir = Path(tmp)
        nodes_file = log_dir / "nodes.json"
        nodes_file.write_text(
            json.dumps({
                "architect": {"active_tasks": 8, "status": "active"},
                "researcher": {"active_tasks": 3, "status": "active"},
                "guardian": {"active_tasks": 0, "status": "idle"},
                "narrator": {"active_tasks": 0, "status": "spawning"},
            }),
            encoding="utf-8",
        )

        scaler = MeshAutoScaler(
            overload_threshold=5,
            idle_threshold=0,
            check_interval=30,
            log_dir=log_dir,
        )

        print("\n── check_load() ─────────────────────────────")
        events = scaler.check_load()
        for evt in events:
            print(f"  [{evt.event_type.upper()}] {evt.node_id}: {evt.reason}")

        print("\n── get_recommendations() ────────────────────")
        for rec in scaler.get_recommendations():
            print(f"  {rec['node_id']:15s} → {rec['event_type']}  ({rec['reason']})")

        autoscaler_log = log_dir / "autoscaler.jsonl"
        if autoscaler_log.exists():
            lines = autoscaler_log.read_text(encoding="utf-8").strip().splitlines()
            print(f"\n── autoscaler.jsonl ({len(lines)} entries) ─────────")
            for line in lines:
                entry = json.loads(line)
                print(f"  {entry['node_id']:15s} {entry['event_type']:12s} ts={entry['timestamp']:.2f}")

        # Demonstrate manual calls
        print("\n── manual scale_up / scale_down ─────────────")
        scaler.scale_up("reviewer")
        scaler.scale_down("narrator")
        for rec in scaler.get_recommendations():
            print(f"  {rec['node_id']:15s} → {rec['event_type']}")
